weeknum = int(input("enter a number between 1 and 7 : "))

if weeknum == 1:
    print("MON")
elif weeknum == 2:
    print("TUE")
elif weeknum == 3:
    print("WED")
elif weeknum == 4:
    print("THU")
elif weeknum == 5:
    print("FRI")
elif weeknum == 6:
    print("SAT")
elif weeknum == 7:
    print("SUN")
else:
    print("!!!Invalid input!!!")
